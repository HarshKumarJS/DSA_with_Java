public class greaterequal_and_lessequal_operator {
    public static void main(String[] args) {

        int a = 8;
        int b = 3;
        System.out.println(a>=b);  // It will return true
        System.out.println(a<=b);  // It will return false

        int c = 5;
        int d = 5;
        System.out.println(c>=d); // It will return true
        System.out.println(c<=d); // It will return true

        int e = 4;
        int f = 9;
        System.out.println(e>=f); // It will return false
        System.out.println(e<=f); // It will return true
    }
}